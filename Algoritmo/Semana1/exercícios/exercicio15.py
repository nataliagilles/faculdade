nota= float(input("Digite a nota do aluno:  "))

if 86 <= nota <= 100:
    print("A nota do aluno é A")

elif 61 <= nota <= 85:
    print("A nota do aluno é B")

elif 36 <= nota <= 60:
    print("A nota do aluno é C")

elif 1 <= nota <= 35:
    print("A nota do aluno é D")

else:
    print("A nota do aluno é E")
        

    


